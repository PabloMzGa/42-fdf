#include "fdf.h"

void	invert_coords(uint32_t *x0, uint32_t *y0, uint32_t *x1, uint32_t *y1)
{
	uint32_t	temp;

	temp = *x0;
	*x0 = *x1;
	*x1 = temp;
	temp = *y0;
	*y0 = *y1;
	*y1 = temp;
}
